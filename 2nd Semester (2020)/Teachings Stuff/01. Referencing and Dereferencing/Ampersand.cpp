#include <iostream> 
using namespace std;

int main()
{
    // This program shows the most basic usage of & operator

        int x = 10;
        cout << "Original x : [ " << x << " ] - Memory Address of x : [ " << &x << " ]" << '\n';

    // Here, "&" operator recieves the "x" variable as an argument, and returns the memory address of the x variable.
}